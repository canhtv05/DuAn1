    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
       int selectedRow = tbl_Luong.getSelectedRow();
    if (selectedRow != -1) {
        int maLuong = Integer.parseInt(tbl_Luong.getValueAt(selectedRow, 1).toString());

        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            if (rpLuong.delete(maLuong) > 0) {
                mesF.showMessage("success", "Xóa thành công");
                fillTable(rpLuong.getAllLuong());
            } else {
                mesF.showMessage("error", "Xóa thất bại");
            }
        }
    } else {
        mesF.showMessage("error", "Chưa chọn dòng nào");
    }
    }//GEN-LAST:event_btn_XoaActionPerformed

