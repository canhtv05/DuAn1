    private void fillTable(ArrayList<Model_Luong> list) {
        TableLuong = (DefaultTableModel) tbl_Luong.getModel();
        totalRows = list.size();
        totalPages = (totalRows + rowsPerPage - 1) / rowsPerPage; // Tính tổng số trang
        int start = (currentPage - 1) * rowsPerPage;
        int end = Math.min(start + rowsPerPage, totalRows);
    TableLuong.setRowCount(0);
    AtomicInteger index = new AtomicInteger(1);
    DecimalFormat formatter = new DecimalFormat("#,###,###");
    // Get attendance data from database
    list.forEach(s -> {
        // Định dạng lương với dấu phân cách hàng nghìn và thêm "Đ" ở cuối
        String formattedLuong = formatter.format(s.getLuongThang()) + " Đ";

        TableLuong.addRow(new Object[]{
            index.getAndIncrement(), s.getMaLuong(), s.getMaNV(), s.getThang(), s.getNam(), s.getSoNgayDiLam(), formattedLuong,s.getHoTen()
        });
    });
    // Cập nhật nhãn trang hiện tại
        jLabel1.setText(String.valueOf(currentPage));
        jLabel3.setText(String.valueOf(totalPages));
    }
